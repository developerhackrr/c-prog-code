//Write a Program using Const Keyword in C//

#include <stdio.h>
int main()
{
const int a =10; //integer constant
const float b =12.3f; // float constant
const char c ='X'; // character constant
const char str[] ="Hello, world!"; // string constant

// printing the values
printf("a = %d\n", a);
printf("b = %f\n", b);
printf("c = %c\n", c);
printf("str = %s\n", str);

return 0;
}

